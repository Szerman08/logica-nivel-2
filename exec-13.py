#13) Ler um valor e escrever a mensagem É MAIOR QUE 10! se o valor lido for maior que 10, caso contrário escrever NÃO É MAIOR QUE 10!

valor = float(input("Digite um valor :"))
if valor < 10:
        print("O numero informado é menor que 10")
else:
        print('O numero informado é maior que 10')


